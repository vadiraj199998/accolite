package com.spring.practice;

public class Rank {

	private int ID;
	private String NAME;
	private int RANK;
	private int TOTAL;
	private String DOB;
	public String getDOB() {
		return DOB;
	}
	public void setDOB(String dOB) {
		DOB = dOB;
	}
	public int getTOTAL() {
		return TOTAL;
	}
	public void setTOTAL(int tOTAL) {
		TOTAL = tOTAL;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	public int getRANK() {
		return RANK;
	}
	public void setRANK(int rANK) {
		RANK = rANK;
	}
	
}
